import { HQ_EXPLODE_RADIUS } from './hq';

// --- ПВО (зенитно-ракетный комплекс), клавиша 6 ---
// Сбивает вражеские ядерные ракеты, чья цель попадает в радиус действия.
// Уровень = сколько ракет можно перехватить до перезарядки; каждый заряд
// восстанавливается параллельно за 7с.
export const SAM_BUILD_TICKS = 50; // 5с на постройку/апгрейд
export const SAM_RELOAD_TICKS = 70; // 7с на восстановление одного заряда
// радиус действия — втрое больше радиуса ядерного взрыва (HQ_EXPLODE_RADIUS*2)
export const SAM_RANGE = HQ_EXPLODE_RADIUS * 6;

// Цена «в общем» по суммарному уровню всех твоих ПВО: 1-я покупка — 1.5млн,
// каждая следующая (новое ПВО или апгрейд) — 3млн
export function samCost(ownedLevels: number): number {
  return ownedLevels <= 0 ? 1_500_000 : 3_000_000;
}
